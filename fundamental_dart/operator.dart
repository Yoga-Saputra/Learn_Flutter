void main() {
  var a = 1;
  var b = 2;
  var c = a + b;

  // Aritmetic operator
  // penjumlahan
  var penjumlahan = a + b;
  print(penjumlahan);

  // pengurangan
  var pengurangan = a - b;
  print(pengurangan);

  // perkalian
  var perkalian = a * b;
  print(perkalian);

  // pembagian
  var pembagian = a / b;
  print(pembagian);

  // modulo
  var sisa = a % b;
  print(sisa);

  // Equality & relational operator
  print(a > b);
  print(a < b);
  print(a == b);
  print(a != b);
  print(a >= b);
  print(a <= b);

  // logical operator
  print('Logical Operators');
  bool x = true;
  bool y = true;

  print(x && y);
  print(x || y);
  print(!x);
}
